<p>For the registration we provide an <a href="page.php?goto=RODBH2019-Registration#en">online form</a>. Participation is open for researchers and students who are interested in the topic of research on online databases in history. There is no participation fee. Participants will be asked to pay for lunch (15 Euro/day) at the Mensa in advance. For those PhD students who submitted a paper or abstract and have been accepted, accommodation is provided as well as parts of the travel expenses which get reimbursed pro rata, funded by the German Science Foundation (DFG).</p>
