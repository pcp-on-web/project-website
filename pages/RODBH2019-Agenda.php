<p>
The Doctoral Symposium is hosted by the Leipzig University of Applied Science, Faculty of Informatics and Media (HTWK) as part of the Research project <a href="https://pcp-on-web.htwk-leipzig.de" target="_blank">Early Modern Professorial Career Patterns - Methodological research on online databases of academic history</a>, in cooperation with the European Consortium on <a href="http://dataforhistory.org/" target="_blank">Data for History</a>. A general meeting of the DfH Consortium is part of the workshop and includes the application of the <a href="http://ontome.dataforhistory.org/" target="_blank">OntoMe - Ontology Management Environment</a>, a platform to design vocabularies using ontologies contributed by the participating Ph.D. students.
</p>

