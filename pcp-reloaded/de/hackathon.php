			<h3>Hackathon (November 2021) und Workshop (10.12.2021 <b>verschoben!</b>)</h3>

<p>
Im Anschluss an die Abschlusstagung führen wir einen Hackathon durch, dessen Ergebnisse im Rahmen eines Workshops am 10. Dezember präsentiert werden. Ziel des Workshops und Hackathons ist die Anreicherung der im Projekt entstanden Forschungsontology bezüglich weiterer Datenquellen und der Interoperabilität für weiterführende Projekte.
</p>
<p>
<b>Ziele und Inhalte</b>
<ul>
<li>Anbindung weiterer prosopographischer Daten (u. a. Professorenkataloge)</li>
<li>Experiment mit Geovistory: RDF-Daten für die Weiterverarbeitung aus Tabellen aufbereiten (David Knecht & Francesco Beretta, Kleio Lab GmbH) <a href="../talks/2021_10_27_Konferenz_Professorale_Karrieren_GV.pdf" target="_blank">Folien</a> </li>
<li>Diskussion des Projekt-Vokabulars z. B. durch Annotation mit relevanten Klassen und Eigenschaften von Foundational Ontologies </li>
<li>Datenimport in die Forschungsontologie (SPARQL queries)</li>
<li>Zeitraum: November 2021</li>
<li>Koordination: Thomas Riechert (thomas.riechert@htwk-leipzig.de)</li>
</ul>
</p>

<p>
<b>Workshopprogramm am 10.12.2021</b>
<ul>
<li><b>10.12.2021 - 9:00 - 10:00</b> Präsentation der Forschungsontologie mit den Daten aus
Helmstedt, Leipzig und Bamberg (Thomas Riechert)</li>
<li><b>10.12.2021 - 10:00 - 12:00</b> Workshop Geovistory (David Knecht, Francesco Beretta)</li>
<li>Mittagspause</li>
<li><b>10.12.2021 - 13:00 - 14:00</b> Präsentation des Vorgehens bei der Erstellung bzw.
Implementierung des Datensatzes , inkl. Präsentation der Werkzeuge (Thomas Riechert, Edgard Marx)</li>
<li><b>10.12.2021 - 14:00 - 14:30</b> Hackathon Abschluss mit Absprachen für den Import weiterer Datenquellen (Thomas Riechert)</li>
</ul>
</p>


