		<h3>Topic</h3>

		<div class="text">
			<p>The DFG research project  “Early Modern Professorial Career Patterns – Methodological Research on Online Databases of Academic History“, collaboratively run by the Herzog August Library Wolfenbüttel and the Hochschule für Technik, Wirtschaft und Kultur in Leipzig, warmly invites you to participate in its closing meeting, to be held on 20-21 March 2023 in Helmstedt.
			</p>
			<p>Dedicated to investigating which conditions were necessary for professors in the early modern university system to attain professional success, within the  project we test how certain scholarly career patterns can be made more visible through the use of methods borrowed from the Digital Humanities. 
			</p>
			<p>Following this premise, we have modeled prosopographic data, as well as data drawn from university and scholarly histories, according to semantic web standards and FAIR data principles. We have also designed an ontology that reflects the specificities of academic life in the early modern period in German-speaking countries, and arranged the existing data accordingly. Both this model, and its relevant concepts and relations, constitute Version 0.2, which can be viewed here.
			</p>
